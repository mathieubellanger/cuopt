def generate_warm_start_message_proto(registry):
    obj = registry.get("lp_solution", {}).get("warm_start", {})
    lines = []
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        num = f.get("field_num")
        if num is not None:
            lines.append((num, f"  repeated double {f['name']} = {num};"))
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        num = f.get("field_num")
        if num is not None:
            lines.append(
                (
                    num,
                    f"  {f.get('type', 'double')} {f['name']} = {num};",
                )
            )
    lines.sort(key=lambda x: x[0])
    return "\n".join(item[1] for item in lines)


def generate_lp_solution_message_proto(registry):
    obj = registry.get("lp_solution", {})
    lines = []
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        num = f.get("field_num")
        if num is not None:
            lines.append((num, f"  repeated double {f['name']} = {num};"))
    lines.extend(_iter_embeds(obj))
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        num = f.get("field_num")
        if num is not None:
            ptype = _solution_scalar_proto_type(registry, f)
            lines.append((num, f"  {ptype} {f['name']} = {num};"))
    lines.sort(key=lambda x: x[0])
    return "\n".join(item[1] for item in lines)


def generate_mip_solution_message_proto(registry):
    obj = registry.get("mip_solution", {})
    lines = []
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
