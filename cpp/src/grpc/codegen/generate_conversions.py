def write_file(path, content):
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    with open(path, "w") as f:
        f.write(content)
    print(f"  wrote {path}")


def parse_field(entry):
    """Parse a field entry from the YAML, which may be a bare string or a dict.

    Does NOT set a default 'type' — callers supply context-appropriate defaults
    (e.g. "double" for scalars, "repeated double" for arrays).
    """
    if isinstance(entry, str):
        return {"name": entry}
    if isinstance(entry, dict):
        assert len(entry) == 1, f"Expected single-key dict, got {entry}"
        name = next(iter(entry))
        val = entry[name]
        if isinstance(val, str):
            return {"name": name, "type": val}
        if isinstance(val, dict):
            result = {"name": name}
            result.update(val)
            return result
        if val is None:
            return {"name": name}
    raise ValueError(f"Unexpected field format: {entry}")


def parse_settings_fields(entries, prefix=""):
    """Parse settings fields, handling nested sub-structs (e.g. tolerances)."""
    for entry in entries:
        assert isinstance(entry, dict) and len(entry) == 1
        name = next(iter(entry))
        val = entry[name]
        if isinstance(val, list):
            yield from parse_settings_fields(val, prefix=f"{prefix}{name}.")
        else:
            f = parse_field(entry)
            if prefix or "member" not in f:
                f["member"] = f"{prefix}{name}" if prefix else name
            yield f


def parse_enum_entry(entry, index=0):
    """Parse a single enum value entry.

    Supports three forms:
      - bare string 'CppName'        → (name, index)
      - {CppName: null}              → (name, index)
      - {CppName: number}            → (name, number)

    The caller is responsible for tracking the running counter; explicit values
    reset it (C-style enum semantics).
    """
    if isinstance(entry, str):
        return entry, index
    assert isinstance(entry, dict) and len(entry) == 1
    name = next(iter(entry))
    num = entry[name]
    return name, num if num is not None else index


def parse_enum_values(values):
    """Parse a full enum values list with C-style auto-numbering.

    Bare names get the next sequential number; explicit {Name: N} overrides
    reset the counter so the next bare name gets N+1.
    """
    counter = 0
    result = []
    for entry in values:
        name, num = parse_enum_entry(entry, index=counter)
        result.append((name, num))
        counter = num + 1
    return result


def camel_to_upper_snake(name):
    """Convert CamelCase to UPPER_SNAKE_CASE: PrimalInfeasible -> PRIMAL_INFEASIBLE."""
    s = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", name)
    s = re.sub(r"([A-Z]+)([A-Z][a-z])", r"\1_\2", s)
    return s.upper()


def _from_proto_cast(ftype):
    """Return C++ cast for reading a proto field back into template type."""
    if ftype in ("double", "float"):
        return "f_t"
    if ftype in ("int32", "int64"):
        return "i_t"
    return None


def _array_element_cast(ftype):
    """Cast for serializing array elements to proto (to_proto direction)."""
    if "double" in ftype:
        return "double"
    if "int32" in ftype:
        return "int32_t"
    return None


def _array_cpp_type(ftype):
    """C++ vector element type for proto-to-C++ direction."""
    if "double" in ftype:
        return "f_t"
    if "int32" in ftype:
        return "i_t"
    return None


def _default_element_size(f):
    """Default element byte size for a field."""
    if "element_size" in f:
        return f["element_size"]
    ftype = f.get("type", "repeated double")
    if "int32" in ftype:
        return 4
    if ftype in ("bytes", "repeated string"):
        return 1
    if ftype.startswith("repeated ") and ftype.split()[-1] not in (
        "double",
        "int32",
        "string",
    ):
        return 4
    return 8


