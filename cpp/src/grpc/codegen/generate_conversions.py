KNOWN_ACRONYMS = {"pdlp", "mip", "lp", "qp", "vrp", "pdp", "tsp"}


def _snake_to_pascal(key):
    """Convert snake_case key to PascalCase, uppercasing known acronyms.

    pdlp_termination_status -> PDLPTerminationStatus
    lp_method               -> LPMethod
    variable_type           -> VariableType
    """
    return "".join(
        part.upper() if part in KNOWN_ACRONYMS else part.capitalize()
        for part in key.split("_")
    )


def _enum_proto_type(key, edef):
    """Derive proto enum type name: explicit or PascalCase from key."""
    return edef.get("proto_type", _snake_to_pascal(key))


def _enum_cpp_type(key, edef):
    """Derive C++ type: explicit or {key}_t."""
    return edef.get("cpp_type", f"{key}_t")


def _enum_default(key, edef):
    """Derive default value: explicit or first value name (proto3 zero-value)."""
    if "default" in edef:
        return edef["default"]
    first = edef["values"][0]
    name, _ = parse_enum_entry(first)
    return name


def _enum_to_proto_fn(key, _edef):
    """Derive to_proto function name: to_proto_{key}."""
    return f"to_proto_{key}"


def _enum_from_proto_fn(key, _edef):
    """Derive from_proto function name: from_proto_{key}."""
    return f"from_proto_{key}"


def _proto_enum_value_name(cpp_name, prefix):
    """Derive proto enum value name from C++ name and prefix."""
    if not prefix:
        return cpp_name
    return f"{prefix}_{camel_to_upper_snake(cpp_name)}"


def _lookup_enum(registry, type_name):
    """Find an enum definition in the registry by key name."""
    return registry.get("enums", {}).get(type_name)


def _is_enum_type(registry, type_name):
    """Check if a type name refers to a registered enum."""
    return type_name in registry.get("enums", {})


def _is_repeated_enum(registry, ftype):
    """Check if a type is 'repeated <enum_key>' and return enum key if so."""
    if ftype.startswith("repeated "):
        inner = ftype.split(None, 1)[1]
        if _is_enum_type(registry, inner):
            return inner
    return None


def _proto_cpp_name(name):
    """Protobuf lowercases all field names for C++ accessor methods."""
    return name.lower()


