def _gen_chunked_to_solution(registry, obj_name, obj, indent="  "):
    """Generate body of chunked_result_to_{lp,mip}_solution()."""
    ind = indent
    lines = []
    constructor_args = obj.get("constructor_args", {})
    ws = obj.get("warm_start")

    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        eid = _field_result_id_name(f)
        lines.append(
            f"{ind}auto {f['name']} = bytes_to_typed<f_t>(arrays, cuopt::remote::{eid});"
        )

    lines.append("")

    scalar_vars = {}
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        if f.get("proto_only"):
            continue
        lines.extend(
            emit_scalar_from_proto_local(
                f"_{name}", f"h.{pname}()", f, registry, ind
            )
        )
        scalar_vars[name] = f"_{name}"

    array_names = [parse_field(e)["name"] for e in obj.get("arrays", [])]
    arg_scalars = constructor_args.get("scalars", [])
    args = [f"std::move({n})" for n in array_names]
    args += [scalar_vars.get(s, f"_{s}") for s in arg_scalars]

    # Warm start — fire reconstruction if any warm-start array is present in
    # the chunked payload. Using arrays.count() (rather than the first array's
    # contents) avoids dropping a payload whose first warm-start array happens
    # to be empty while others carry data.
    if ws and ws.get("arrays"):
        ws_arrays = ws["arrays"]
        ws_rid_prefix = ws.get("result_id_prefix", "")
        ws_ch_prefix = ws.get("chunked_header_prefix", "")
        detect_eids = [
            _field_result_id_name(parse_field(e), prefix=ws_rid_prefix)
            for e in ws_arrays
        ]
        lines.append("")
        detect_expr = " || ".join(
            f"arrays.count(cuopt::remote::{eid}) != 0" for eid in detect_eids
        )
        lines.append(f"{ind}bool _ws_present = {detect_expr};")
        lines.append(f"{ind}if (_ws_present) {{")
        lines.append(f"{ind}  cpu_pdlp_warm_start_data_t<i_t, f_t> ws;")

        for entry in ws_arrays:
            f = parse_field(entry)
            member = f.get("member", f["name"])
            eid = _field_result_id_name(f, prefix=ws_rid_prefix)
            lines.append(
                f"{ind}  ws.{member} = bytes_to_typed<f_t>(arrays, cuopt::remote::{eid});"
            )

        for entry in ws.get("scalars", []):
            f = parse_field(entry)
            ch_name = f"{ws_ch_prefix}{f['name']}"
            pname = _proto_cpp_name(ch_name)
            member = f.get("member", f["name"])
            ftype = f.get("type", "double")
            cast = _from_proto_cast(ftype)
            if cast:
                lines.append(
                    f"{ind}  ws.{member} = static_cast<{cast}>(h.{pname}());"
                )
            else:
                lines.append(f"{ind}  ws.{member} = h.{pname}();")

        cpp_type = obj["cpp_type"]
        ws_args = args + ["std::move(ws)"]
        lines.append(f"{ind}  return {cpp_type}({', '.join(ws_args)});")
        lines.append(f"{ind}}}")
        lines.append("")

    cpp_type = obj["cpp_type"]
    lines.append(f"{ind}return {cpp_type}({', '.join(args)});")
    return "\n".join(lines)


