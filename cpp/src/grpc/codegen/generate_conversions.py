def main():
    parser = argparse.ArgumentParser(
        description="Generate conversion code from field registry"
    )
    parser.add_argument(
        "--registry",
        default=str(Path(__file__).parent / "field_registry.yaml"),
    )
    parser.add_argument(
        "--output-dir", default=str(Path(__file__).parent / "generated")
    )
    parser.add_argument(
        "--auto-number",
        action="store_true",
        help="Auto-assign missing field_num and array_id values, writing "
        "them back to the registry YAML (preserves comments/formatting).",
    )
    parser.add_argument(
        "--strip",
        action="store_true",
        help="Remove all field_num and array_id values from the registry YAML "
        "and exit. Useful before committing a minimal registry.",
    )
    args = parser.parse_args()

    if args.strip:
        n = strip_field_numbers_text(args.registry)
        if n > 0:
            print(f"  stripped {n} field number(s) from {args.registry}")
        else:
            print("  no field numbers to strip")
        sys.exit(0)

    if args.auto_number:
        try:
            from ruamel.yaml import YAML
        except ImportError:
            print(
                "ERROR: --auto-number requires ruamel.yaml. "
                "Install with: pip install ruamel.yaml",
                file=sys.stderr,
            )
            sys.exit(1)

        ryaml = YAML(typ="rt")
        ryaml.preserve_quotes = True
        with open(args.registry) as f:
            rt_data = ryaml.load(f)

        n = auto_assign_field_numbers(rt_data)
        if n > 0:
            _dump_and_verify(ryaml, rt_data, args.registry)
            print(f"  auto-numbered {n} field(s) in {args.registry}")
        else:
            print("  all field numbers already assigned")

    with open(args.registry) as f:
        registry = yaml.safe_load(f)

    _normalize_registry_callables(registry)

    if not _registry_has_field_numbers(registry):
        print(
            "ERROR: Registry has no field_num or array_id values assigned.\n"
            "Run with --auto-number to assign them before generating.",
            file=sys.stderr,
        )
        sys.exit(1)

    try:
        _validate_registry_uniqueness(registry)
    except ValueError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        sys.exit(1)

    outdir = args.output_dir

    # Proto reference .inc files
    write_file(
        os.path.join(outdir, "generated_result_enums.proto.inc"),
        HEADER
        + "// ResultFieldId enum entries\n"
        + generate_proto_result_enums(registry)
        + "\n",
    )

    # Settings conversion .inc
    for key, label in [("pdlp_settings", "pdlp"), ("mip_settings", "mip")]:
        if key in registry:
            obj = registry[key]
            write_file(
                os.path.join(
                    outdir, f"generated_{label}_settings_to_proto.inc"
                ),
                HEADER
                + generate_settings_to_proto_body(registry, key, obj)
                + "\n",
            )
            write_file(
                os.path.join(
                    outdir, f"generated_proto_to_{label}_settings.inc"
                ),
                HEADER
                + generate_proto_to_settings_body(registry, key, obj)
                + "\n",
            )

    # Full data proto
    write_file(
        os.path.join(outdir, "cuopt_remote_data.proto"),
        generate_data_proto(registry),
    )

    # Per-domain enum converters
    for domain in ("settings", "solution", "problem"):
        write_file(
            os.path.join(outdir, f"generated_enum_converters_{domain}.inc"),
            HEADER + generate_enum_converters_inc(registry, domain) + "\n",
        )

    # Solution conversion .inc files
    for key, label in [("lp_solution", "lp"), ("mip_solution", "mip")]:
        obj = registry.get(key)
        if not obj:
            continue
        write_file(
            os.path.join(outdir, f"generated_{label}_solution_to_proto.inc"),
            HEADER + _gen_solution_to_proto(registry, key, obj) + "\n",
        )
        write_file(
            os.path.join(outdir, f"generated_proto_to_{label}_solution.inc"),
            HEADER + _gen_proto_to_solution(registry, key, obj) + "\n",
        )
        write_file(
            os.path.join(outdir, f"generated_{label}_chunked_header.inc"),
            HEADER + _gen_chunked_header(registry, key, obj) + "\n",
        )
        write_file(
            os.path.join(outdir, f"generated_collect_{label}_arrays.inc"),
            HEADER + _gen_collect_arrays(registry, key, obj) + "\n",
        )
        write_file(
            os.path.join(outdir, f"generated_chunked_to_{label}_solution.inc"),
            HEADER + _gen_chunked_to_solution(registry, key, obj) + "\n",
        )

    # Problem conversion .inc files
    if "optimization_problem" in registry:
        write_file(
            os.path.join(outdir, "generated_problem_to_proto.inc"),
            HEADER + _gen_problem_to_proto(registry) + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_proto_to_problem.inc"),
            HEADER + _gen_proto_to_problem(registry) + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_estimate_problem_size.inc"),
            HEADER + _gen_estimate_problem_size(registry) + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_populate_chunked_header_lp.inc"),
            HEADER + _gen_populate_chunked_header(registry, "lp") + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_populate_chunked_header_mip.inc"),
            HEADER + _gen_populate_chunked_header(registry, "mip") + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_chunked_header_to_problem.inc"),
            HEADER + _gen_chunked_header_to_problem(registry) + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_chunked_arrays_to_problem.inc"),
            HEADER + _gen_chunked_arrays_to_problem(registry) + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_build_array_chunks.inc"),
            HEADER + _gen_build_array_chunk_requests(registry) + "\n",
        )
        write_file(
            os.path.join(outdir, "generated_array_field_element_size.inc"),
            HEADER + generate_array_field_element_size_inc(registry) + "\n",
        )

    print(f"\nDone! Generated {len(os.listdir(outdir))} files in: {outdir}")


if __name__ == "__main__":
    main()
