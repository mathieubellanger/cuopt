def _gen_solution_to_proto(registry, obj_name, obj, indent="  "):
    """Generate body of map_{lp,mip}_solution_to_proto()."""
    ind = indent
    lines = []

    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        getter = f.get("getter", f"get_{name}()")
        lines.extend(
            emit_scalar_to_proto(
                f"pb_solution->set_{pname}",
                f"solution.{getter}",
                f,
                registry,
                ind,
            )
        )

    lines.append("")

    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        getter = _default_solution_array_getter(f)
        var = f"_{f['name']}"
        lines.append(f"{ind}const auto& {var} = solution.{getter};")
        lines.append(
            f"{ind}for (const auto& v : {var}) pb_solution->add_{pname}(static_cast<double>(v));"
        )

    # Warm start
    ws = obj.get("warm_start")
    if ws:
        check = ws.get("presence_check", "has_warm_start_data()")
        ws_getter = ws.get("getter", "get_cpu_pdlp_warm_start_data()")
        lines.append("")
        lines.append(f"{ind}if (solution.{check}) {{")
        lines.append(
            f"{ind}  auto* pb_ws = pb_solution->mutable_warm_start_data();"
        )
        lines.append(f"{ind}  const auto& ws = solution.{ws_getter};")
        for entry in ws.get("arrays", []):
            f = parse_field(entry)
            pname = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            lines.append(
                f"{ind}  for (const auto& v : ws.{member}) pb_ws->add_{pname}(static_cast<double>(v));"
            )
        for entry in ws.get("scalars", []):
            f = parse_field(entry)
            pname = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            ftype = f.get("type", "double")
            cast = (
                "double"
                if ftype == "double"
                else ("int32_t" if ftype == "int32" else "double")
            )
            lines.append(
                f"{ind}  pb_ws->set_{pname}(static_cast<{cast}>(ws.{member}));"
            )
        lines.append(f"{ind}}}")

    return "\n".join(lines)


def _gen_proto_to_solution(registry, obj_name, obj, indent="  "):
    """Generate body of map_proto_to_{lp,mip}_solution()."""
    ind = indent
    lines = []
    cpp_type = obj["cpp_type"]
    constructor_args = obj.get("constructor_args", {})
    ws = obj.get("warm_start")

    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        lines.append(
            f"{ind}std::vector<f_t> {f['name']}(pb_solution.{pname}().begin(), pb_solution.{pname}().end());"
        )

    lines.append("")

    scalar_vars = {}
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        if f.get("proto_only"):
            continue
        lines.extend(
            emit_scalar_from_proto_local(
                f"_{name}", f"pb_solution.{pname}()", f, registry, ind
            )
        )
        scalar_vars[name] = f"_{name}"

    array_names = [parse_field(e)["name"] for e in obj.get("arrays", [])]
    arg_scalars = constructor_args.get("scalars", [])
    args = [f"std::move({n})" for n in array_names]
    args += [scalar_vars.get(s, f"_{s}") for s in arg_scalars]

    if ws:
        accessor_getter = "warm_start_data"
        lines.append("")
        lines.append(f"{ind}if (pb_solution.has_{accessor_getter}()) {{")
        lines.append(
            f"{ind}  const auto& pb_ws = pb_solution.{accessor_getter}();"
        )
        lines.append(f"{ind}  cpu_pdlp_warm_start_data_t<i_t, f_t> ws;")

        for entry in ws.get("arrays", []):
            f = parse_field(entry)
            pname = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            lines.append(
                f"{ind}  ws.{member}.assign(pb_ws.{pname}().begin(), pb_ws.{pname}().end());"
            )

        for entry in ws.get("scalars", []):
            f = parse_field(entry)
            pname = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            ftype = f.get("type", "double")
            cast = _from_proto_cast(ftype)
            if cast:
                lines.append(
                    f"{ind}  ws.{member} = static_cast<{cast}>(pb_ws.{pname}());"
                )
            else:
                lines.append(f"{ind}  ws.{member} = pb_ws.{pname}();")

        ws_args = args + ["std::move(ws)"]
        lines.append(f"{ind}  return {cpp_type}({', '.join(ws_args)});")
        lines.append(f"{ind}}}")
        lines.append("")

    lines.append(f"{ind}return {cpp_type}({', '.join(args)});")
    return "\n".join(lines)


