def _gen_problem_to_proto(registry, indent="  "):
    """Generate body of map_problem_to_proto()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []
    setter_groups = obj.get("setter_groups", {})
    grouped_fields = set()
    for gdef in setter_groups.values():
        for fname in gdef.get("fields", []):
            grouped_fields.add(fname)

    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        getter = _default_problem_getter(f, is_scalar=True)
        setter_lhs = f"pb_problem->set_{pname}"
        lines.extend(
            emit_scalar_to_proto(
                setter_lhs, f"cpu_problem.{getter}", f, registry, ind
            )
        )

    lines.append("")

    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        if name in grouped_fields or f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        getter = _default_problem_getter(f, is_scalar=False)

        enum_key = _is_repeated_enum(registry, ftype)
        if enum_key:
            edef = _lookup_enum(registry, enum_key)
            to_fn = _enum_to_proto_fn(enum_key, edef)
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = cpu_problem.{getter};")
            lines.append(
                f"{ind}  for (const auto& v : _{name}) pb_problem->add_{pname}({to_fn}(v));"
            )
            lines.append(f"{ind}}}")
        elif ftype == "repeated string":
            lines.append(
                f"{ind}for (const auto& s : cpu_problem.{getter}) pb_problem->add_{pname}(s);"
            )
        elif ftype == "bytes":
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = cpu_problem.{getter};")
            lines.append(f"{ind}  if (!_{name}.empty()) {{")
            lines.append(
                f"{ind}    pb_problem->set_{pname}(std::string(_{name}.begin(), _{name}.end()));"
            )
            lines.append(f"{ind}  }}")
            lines.append(f"{ind}}}")
        elif ftype.startswith("repeated"):
            cast = _array_element_cast(ftype)
            var = f"_{name}"
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto {var} = cpu_problem.{getter};")
            lines.append(
                f"{ind}  for (const auto& v : {var}) pb_problem->add_{pname}(static_cast<{cast}>(v));"
            )
            lines.append(f"{ind}}}")

    # Setter groups
    for gname, gdef in setter_groups.items():
        fields = [
            _find_problem_field(obj, fn) for fn in gdef.get("fields", [])
        ]
        for f in fields:
            if f is None:
                continue
            pname = _proto_cpp_name(f["name"])
            getter = _default_problem_getter(f, is_scalar=False)
            ftype = f.get("type", "repeated double")
            cast = _array_element_cast(ftype)
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{f['name']} = cpu_problem.{getter};")
            lines.append(
                f"{ind}  for (const auto& v : _{f['name']}) pb_problem->add_{pname}(static_cast<{cast}>(v));"
            )
            lines.append(f"{ind}}}")

    # repeated_messages — emit per-entry encode loop (unary path: scalars and
    # arrays both go inline into the nested proto message).
    for name, body in _iter_repeated_messages(obj):
        presence = body.get("presence_check")
        getter = body.get("getter")
        if not presence or not getter:
            raise ValueError(
                f"repeated_messages.{name}: requires both 'presence_check' "
                f"and 'getter' for unary encode"
            )
        lines.append("")
        lines.append(f"{ind}if (cpu_problem.{presence}) {{")
        lines.append(
            f"{ind}  for (const auto& _entry : cpu_problem.{getter}) {{"
        )
        lines.append(f"{ind}    auto* pb_entry = pb_problem->add_{name}();")
        for f in _repeated_message_scalars(body):
            sp = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            to_cast = _resolved_to_proto_cast(f)
            if to_cast:
                lines.append(
                    f"{ind}    pb_entry->set_{sp}(static_cast<{to_cast}>(_entry.{member}));"
                )
            else:
                lines.append(f"{ind}    pb_entry->set_{sp}(_entry.{member});")
        for f in _repeated_message_arrays(body):
            ap = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            ftype = f.get("type", "repeated double")
            cast = _array_element_cast(ftype) or "double"
            lines.append(
                f"{ind}    for (const auto& v : _entry.{member}) "
                f"pb_entry->add_{ap}(static_cast<{cast}>(v));"
            )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}}}")

    return "\n".join(lines)


def _gen_proto_to_problem(registry, indent="  "):
    """Generate body of map_proto_to_problem()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []
    setter_groups = obj.get("setter_groups", {})
    grouped_fields = set()
    for gdef in setter_groups.values():
        for fname in gdef.get("fields", []):
            grouped_fields.add(fname)
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        setter = _default_problem_setter(f)
        lines.extend(
            emit_scalar_from_proto_assign(
                lambda v, s=setter: f"cpu_problem.{s}({v});",
                f"pb_problem.{pname}()",
                f,
                registry,
                ind,
                has_check=f"pb_problem.has_{pname}()",
            )
        )

    lines.append("")

    # Setter groups — guard on the structural sentinel (offsets). For CSR-style
    # groups, offsets is non-empty even when nnz=0; using values/indices as the
    # sentinel would silently drop zero-nnz matrices.
    for _gname, gdef in setter_groups.items():
        setter_name = gdef["setter"]
        fields = [
            _find_problem_field(obj, fn) for fn in gdef.get("fields", [])
        ]
        sentinel = _group_presence_field(fields)
        if sentinel is None:
            continue
        sentinel_pname = _proto_cpp_name(sentinel["name"])
        lines.append(f"{ind}if (pb_problem.{sentinel_pname}_size() > 0) {{")
        ii = ind + "  "

        for f in fields:
            if f is None:
                continue
            pname = _proto_cpp_name(f["name"])
            ftype = f.get("type", "repeated double")
            cpp_t = _array_cpp_type(ftype)
            lines.append(
                f"{ii}std::vector<{cpp_t}> {f['name']}(pb_problem.{pname}().begin(), pb_problem.{pname}().end());"
            )

        values_f, indices_f = _group_companion_pair(fields)
        if values_f is not None and indices_f is not None:
            lines.append(
                f"{ii}if ({values_f['name']}.size() != {indices_f['name']}.size()) {{"
            )
            lines.append(
                f'{ii}  throw std::invalid_argument("{setter_name}: values/indices size mismatch");'
            )
            lines.append(f"{ii}}}")

        args = []
        for f in fields:
            if f is None:
                continue
            args.append(f"{f['name']}.data()")
            args.append(f"static_cast<i_t>({f['name']}.size())")
        lines.append(f"{ii}cpu_problem.{setter_name}({', '.join(args)});")
        lines.append(f"{ind}}}")
        lines.append("")

    # Non-grouped arrays
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        if name in grouped_fields or f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        setter = _default_problem_setter(f)

        enum_key = _is_repeated_enum(registry, ftype)
        if enum_key:
            edef = _lookup_enum(registry, enum_key)
            from_fn = _enum_from_proto_fn(enum_key, edef)
            cpp_type = _enum_cpp_type(enum_key, edef)
            lines.append(f"{ind}if (pb_problem.{pname}_size() > 0) {{")
            lines.append(f"{ind}  std::vector<{cpp_type}> {name};")
            lines.append(f"{ind}  {name}.reserve(pb_problem.{pname}_size());")
            proto_type = _enum_proto_type(enum_key, edef)
            lines.append(
                f"{ind}  for (const auto& v : pb_problem.{pname}()) {{"
            )
            lines.append(
                f"{ind}    {name}.push_back({from_fn}(static_cast<cuopt::remote::{proto_type}>(v)));"
            )
            lines.append(f"{ind}  }}")
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}.data(), static_cast<i_t>({name}.size()));"
            )
            lines.append(f"{ind}}}")
        elif ftype == "repeated string":
            lines.append(f"{ind}if (pb_problem.{pname}_size() > 0) {{")
            lines.append(
                f"{ind}  std::vector<std::string> {name}(pb_problem.{pname}().begin(), pb_problem.{pname}().end());"
            )
            lines.append(f"{ind}  cpu_problem.{setter}({name});")
            lines.append(f"{ind}}}")
        elif ftype == "bytes":
            lines.append(f"{ind}if (!pb_problem.{pname}().empty()) {{")
            lines.append(
                f"{ind}  const std::string& {name}_str = pb_problem.{pname}();"
            )
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}_str.data(), static_cast<i_t>({name}_str.size()));"
            )
            lines.append(f"{ind}}}")
        elif ftype.startswith("repeated"):
            cpp_t = _array_cpp_type(ftype)
            guard = f.get("conditional")
            open_brace = (
                f"if (pb_problem.{pname}_size() > 0) {{" if guard else "{"
            )
            lines.append(f"{ind}{open_brace}")
            lines.append(
                f"{ind}  std::vector<{cpp_t}> {name}(pb_problem.{pname}().begin(), pb_problem.{pname}().end());"
            )
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}.data(), static_cast<i_t>({name}.size()));"
            )
            lines.append(f"{ind}}}")

    # repeated_messages — emit per-entry decode loop (unary path: scalars and
    # arrays are both read inline from the nested proto message).
    for name, body in _iter_repeated_messages(obj):
        setter = body.get("setter")
        cpp_inner = body.get("cpp_inner_type")
        if not setter or not cpp_inner:
            raise ValueError(
                f"repeated_messages.{name}: requires both 'setter' and "
                f"'cpp_inner_type' for unary decode"
            )
        lines.append("")
        lines.append(f"{ind}if (pb_problem.{name}_size() > 0) {{")
        lines.append(f"{ind}  std::vector<{cpp_inner}> _entries;")
        lines.append(f"{ind}  _entries.reserve(pb_problem.{name}_size());")
        lines.append(
            f"{ind}  for (const auto& pb_entry : pb_problem.{name}()) {{"
        )
        lines.append(f"{ind}    {cpp_inner} _entry;")
        for f in _repeated_message_scalars(body):
            sp = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            from_cast = f.get("from_proto_cast")
            ftype = f.get("type", "double")
            if from_cast:
                lines.append(
                    f"{ind}    _entry.{member} = static_cast<{from_cast}>(pb_entry.{sp}());"
                )
            elif _from_proto_cast(ftype):
                lines.append(
                    f"{ind}    _entry.{member} = static_cast<{_from_proto_cast(ftype)}>(pb_entry.{sp}());"
                )
            else:
                lines.append(f"{ind}    _entry.{member} = pb_entry.{sp}();")
        for f in _repeated_message_arrays(body):
            ap = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            lines.append(
                f"{ind}    _entry.{member}.assign(pb_entry.{ap}().begin(), pb_entry.{ap}().end());"
            )
        for a, b in _repeated_message_companion_pairs(name, body):
            a_mem = a.get("member", a["name"])
            b_mem = b.get("member", b["name"])
            lines.append(
                f"{ind}    if (_entry.{a_mem}.size() != _entry.{b_mem}.size()) {{"
            )
            lines.append(
                f'{ind}      throw std::invalid_argument("{setter}: {a["name"]}/'
                f'{b["name"]} size mismatch");'
            )
            lines.append(f"{ind}    }}")
        if name == "quadratic_constraints":
            lines.append(
                f"{ind}    io::canonicalize_coo_matrix(_entry.rows, _entry.cols, _entry.vals);"
            )
        lines.append(f"{ind}    _entries.push_back(std::move(_entry));")
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}  cpu_problem.{setter}(std::move(_entries));")
        lines.append(f"{ind}}}")

    return "\n".join(lines)


def _gen_estimate_problem_size(registry, indent="  "):
    """Generate body of estimate_problem_proto_size().

    Each emitted line is annotated with an inline comment explaining what
    the literal represents in protobuf wire format.  See PROTO_SIZE and
    SIZE_ESTIMATOR_BODY_DOC for the size model.
    """
    obj = registry.get("optimization_problem", {})
    ind = indent
    vi = PROTO_SIZE["int32_varint"]
    ev = PROTO_SIZE["enum_varint"]
    sf = PROTO_SIZE["string_framing"]
    pf = PROTO_SIZE["per_entry_framing"]
    env = PROTO_SIZE["outer_envelope"]

    lines = []
    for doc_line in SIZE_ESTIMATOR_BODY_DOC.rstrip("\n").splitlines():
        lines.append(f"{ind}{doc_line}")
    lines.append(f"{ind}size_t est = 0;")

    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        if f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        getter = _default_problem_getter(f, is_scalar=False)
        if ftype == "repeated string":
            lines.append(
                f"{ind}for (const auto& s : cpu_problem.{getter}) "
                f"est += s.size() + {sf};"
                f"  // string field, length-delimited (body + {sf}B framing)"
            )
        elif ftype == "bytes":
            lines.append(
                f"{ind}est += cpu_problem.{getter}.size();"
                f"  // bytes field, length-delimited (payload only; framing in envelope)"
            )
        elif "int32" in ftype:
            lines.append(
                f"{ind}est += cpu_problem.{getter}.size() * {vi};"
                f"  // int32 field, varint (max {vi}B per value)"
            )
        elif ftype.startswith("repeated ") and _is_repeated_enum(
            registry, ftype
        ):
            lines.append(
                f"{ind}est += cpu_problem.{getter}.size() * {ev};"
                f"  // enum field, varint ({ev}B per value, generous for short enums)"
            )
        else:
            lines.append(
                f"{ind}est += cpu_problem.{getter}.size() * sizeof(double);"
                f"  // double field, fixed64 (8B per value)"
            )

    # repeated_messages — accumulate per-entry scalar + array contributions.
    # Per-entry overhead is the nested message wrapper; inner scalars/arrays
    # contribute their own wire sizes inside the loop.
    for name, body in _iter_repeated_messages(obj):
        presence = body.get("presence_check")
        getter = body.get("getter")
        if not presence or not getter:
            continue
        lines.append(f"{ind}if (cpu_problem.{presence}) {{")
        lines.append(
            f"{ind}  for (const auto& _entry : cpu_problem.{getter}) {{"
        )
        lines.append(
            f"{ind}    est += {pf};"
            f"  // per-entry framing: {pf}B slop budget"
            f" for nested message header"
        )
        for f in _repeated_message_scalars(body):
            member = f.get("member", f["name"])
            ftype = f.get("type", "double")
            if ftype == "string":
                lines.append(
                    f"{ind}    est += _entry.{member}.size() + {sf};"
                    f"  // {member}: string field, length-delimited"
                    f" (body + {sf}B framing)"
                )
            elif "int" in ftype:
                lines.append(
                    f"{ind}    est += {vi};"
                    f"  // {member}: int32 field, varint"
                    f" (max {vi}B per value)"
                )
            else:
                lines.append(
                    f"{ind}    est += sizeof(double);"
                    f"  // {member}: double field, fixed64 (8B per value)"
                )
        for f in _repeated_message_arrays(body):
            member = f.get("member", f["name"])
            ftype = f.get("type", "repeated double")
            if "int32" in ftype:
                lines.append(
                    f"{ind}    est += _entry.{member}.size() * {vi};"
                    f"  // int32 field, varint (max {vi}B per value)"
                )
            else:
                lines.append(
                    f"{ind}    est += _entry.{member}.size() * sizeof(double);"
                    f"  // double field, fixed64 (8B per value)"
                )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}}}")

    lines.append(
        f"{ind}est += {env};"
        f"  // outer message envelope: {env}B slop budget"
        f" for header + settings + scalar fields"
    )
    lines.append(f"{ind}return est;")
    return "\n".join(lines)


def _gen_populate_chunked_header(registry, solver_type, indent="  "):
    """Generate body of populate_chunked_header_{lp,mip}()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []

    lines.append(f"{ind}auto* rh = header->mutable_header();")
    lines.append(f"{ind}rh->set_version(1);")
    if solver_type == "lp":
        lines.append(f"{ind}rh->set_problem_category(cuopt::remote::LP);")
    else:
        lines.append(f"{ind}rh->set_problem_category(cuopt::remote::MIP);")

    lines.append("")

    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        getter = _default_problem_getter(f, is_scalar=True)
        lines.extend(
            emit_scalar_to_proto(
                f"header->set_{pname}",
                f"cpu_problem.{getter}",
                f,
                registry,
                ind,
            )
        )

    lines.append("")

    if solver_type == "lp":
        lines.append(
            f"{ind}map_pdlp_settings_to_proto(settings, header->mutable_lp_settings());"
        )
    else:
        lines.append(
            f"{ind}map_mip_settings_to_proto(settings, header->mutable_mip_settings());"
        )
        lines.append(f"{ind}header->set_enable_incumbents(enable_incumbents);")

    # repeated_messages — populate per-entry scalars in the header.  Arrays
    # for each entry travel separately via container-keyed ArrayChunks.
    for name, body in _iter_repeated_messages(obj):
        presence = body.get("presence_check")
        getter = body.get("getter")
        if not presence or not getter:
            continue
        lines.append("")
        lines.append(f"{ind}if (cpu_problem.{presence}) {{")
        lines.append(
            f"{ind}  for (const auto& _entry : cpu_problem.{getter}) {{"
        )
        lines.append(f"{ind}    auto* pb_entry = header->add_{name}();")
        for f in _repeated_message_scalars(body):
            sp = _proto_cpp_name(f["name"])
            member = f.get("member", f["name"])
            to_cast = _resolved_to_proto_cast(f)
            if to_cast:
                lines.append(
                    f"{ind}    pb_entry->set_{sp}(static_cast<{to_cast}>(_entry.{member}));"
                )
            else:
                lines.append(f"{ind}    pb_entry->set_{sp}(_entry.{member});")
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}}}")

    return "\n".join(lines)


def _gen_chunked_header_to_problem(registry, indent="  "):
    """Generate body of map_chunked_header_to_problem()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []

    # `ChunkedProblemHeader` is hand-written (see cuopt_remote_service.proto);
    # it does not declare `optional` for any field, so we pass has_check=None
    # to suppress the has_X() guard.  `sentinel:` still applies — the wire
    # mapping must stay consistent with the unary path.
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        setter = _default_problem_setter(f)
        lines.extend(
            emit_scalar_from_proto_assign(
                lambda v, s=setter: f"cpu_problem.{s}({v});",
                f"header.{pname}()",
                f,
                registry,
                ind,
                has_check=None,
            )
        )

    lines.append("")

    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        if f.get("type") != "repeated string":
            continue
        name = f["name"]
        pname = _proto_cpp_name(name)
        setter = _default_problem_setter(f)
        lines.append(f"{ind}if (header.{pname}_size() > 0) {{")
        lines.append(
            f"{ind}  std::vector<std::string> {name}(header.{pname}().begin(), header.{pname}().end());"
        )
        lines.append(f"{ind}  cpu_problem.{setter}({name});")
        lines.append(f"{ind}}}")

    return "\n".join(lines)


def _gen_chunked_arrays_to_problem(registry, indent="  "):
    """Generate body of map_chunked_arrays_to_problem()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []
    setter_groups = obj.get("setter_groups", {})
    grouped_fields = set()
    for gdef in setter_groups.values():
        for fname in gdef.get("fields", []):
            grouped_fields.add(fname)
    lines.append(f"{ind}map_chunked_header_to_problem(header, cpu_problem);")
    lines.append("")

    # Lambda helpers
    lines.append(
        f"{ind}auto get_doubles = [&](int32_t field_id) -> std::vector<f_t> {{"
    )
    lines.append(f"{ind}  auto it = arrays.find(field_id);")
    lines.append(
        f"{ind}  if (it == arrays.end() || it->second.empty()) return {{}};"
    )
    lines.append(f"{ind}  if (it->second.size() % sizeof(double) != 0) {{")
    lines.append(
        f'{ind}    throw std::invalid_argument("get_doubles: payload size " + '
        f'std::to_string(it->second.size()) + " for field_id " + '
        f'std::to_string(field_id) + " is not a multiple of sizeof(double)");'
    )
    lines.append(f"{ind}  }}")
    lines.append(f"{ind}  size_t n = it->second.size() / sizeof(double);")
    lines.append(f"{ind}  if constexpr (std::is_same_v<f_t, double>) {{")
    lines.append(f"{ind}    std::vector<double> v(n);")
    lines.append(
        f"{ind}    std::memcpy(v.data(), it->second.data(), n * sizeof(double));"
    )
    lines.append(f"{ind}    return v;")
    lines.append(f"{ind}  }} else {{")
    lines.append(f"{ind}    std::vector<double> tmp(n);")
    lines.append(
        f"{ind}    std::memcpy(tmp.data(), it->second.data(), n * sizeof(double));"
    )
    lines.append(f"{ind}    return std::vector<f_t>(tmp.begin(), tmp.end());")
    lines.append(f"{ind}  }}")
    lines.append(f"{ind}}};")
    lines.append("")

    lines.append(
        f"{ind}auto get_ints = [&](int32_t field_id) -> std::vector<i_t> {{"
    )
    lines.append(f"{ind}  auto it = arrays.find(field_id);")
    lines.append(
        f"{ind}  if (it == arrays.end() || it->second.empty()) return {{}};"
    )
    lines.append(f"{ind}  if (it->second.size() % sizeof(int32_t) != 0) {{")
    lines.append(
        f'{ind}    throw std::invalid_argument("get_ints: payload size " + '
        f'std::to_string(it->second.size()) + " for field_id " + '
        f'std::to_string(field_id) + " is not a multiple of sizeof(int32_t)");'
    )
    lines.append(f"{ind}  }}")
    lines.append(f"{ind}  size_t n = it->second.size() / sizeof(int32_t);")
    lines.append(f"{ind}  if constexpr (std::is_same_v<i_t, int32_t>) {{")
    lines.append(f"{ind}    std::vector<int32_t> v(n);")
    lines.append(
        f"{ind}    std::memcpy(v.data(), it->second.data(), n * sizeof(int32_t));"
    )
    lines.append(f"{ind}    return v;")
    lines.append(f"{ind}  }} else {{")
    lines.append(f"{ind}    std::vector<int32_t> tmp(n);")
    lines.append(
        f"{ind}    std::memcpy(tmp.data(), it->second.data(), n * sizeof(int32_t));"
    )
    lines.append(f"{ind}    return std::vector<i_t>(tmp.begin(), tmp.end());")
    lines.append(f"{ind}  }}")
    lines.append(f"{ind}}};")
    lines.append("")

    lines.append(
        f"{ind}auto get_bytes = [&](int32_t field_id) -> std::string {{"
    )
    lines.append(f"{ind}  auto it = arrays.find(field_id);")
    lines.append(
        f"{ind}  if (it == arrays.end() || it->second.empty()) return {{}};"
    )
    lines.append(
        f"{ind}  return std::string(reinterpret_cast<const char*>(it->second.data()), it->second.size());"
    )
    lines.append(f"{ind}}};")
    lines.append("")

    lines.append(
        f"{ind}auto get_string_list = [&](int32_t field_id) -> std::vector<std::string> {{"
    )
    lines.append(f"{ind}  auto it = arrays.find(field_id);")
    lines.append(
        f"{ind}  if (it == arrays.end() || it->second.empty()) return {{}};"
    )
    lines.append(f"{ind}  std::vector<std::string> names;")
    lines.append(
        f"{ind}  const char* s     = reinterpret_cast<const char*>(it->second.data());"
    )
    lines.append(f"{ind}  const char* s_end = s + it->second.size();")
    lines.append(f"{ind}  while (s < s_end) {{")
    lines.append(
        f"{ind}    const char* nul = static_cast<const char*>(std::memchr(s, '\\0', s_end - s));"
    )
    lines.append(f"{ind}    if (!nul) nul = s_end;")
    lines.append(f"{ind}    names.emplace_back(s, nul);")
    lines.append(f"{ind}    if (nul == s_end) break;")
    lines.append(f"{ind}    s = nul + 1;")
    lines.append(f"{ind}  }}")
    lines.append(f"{ind}  return names;")
    lines.append(f"{ind}}};")
    lines.append("")

    # Setter groups — guard on the structural sentinel (offsets). For CSR-style
    # groups, offsets is non-empty even when nnz=0; requiring all companion
    # arrays to be non-empty would silently drop zero-nnz matrices.
    for _gname, gdef in setter_groups.items():
        setter_name = gdef["setter"]
        fields = [
            _find_problem_field(obj, fn) for fn in gdef.get("fields", [])
        ]
        sentinel = _group_presence_field(fields)
        if sentinel is None:
            continue

        for f in fields:
            if f is None:
                continue
            ftype = f.get("type", "repeated double")
            afid = _field_array_id_name(f)
            extract_fn = "get_doubles" if "double" in ftype else "get_ints"
            lines.append(
                f"{ind}auto {f['name']} = {extract_fn}(cuopt::remote::{afid});"
            )

        lines.append(f"{ind}if (!{sentinel['name']}.empty()) {{")

        values_f, indices_f = _group_companion_pair(fields)
        if values_f is not None and indices_f is not None:
            lines.append(
                f"{ind}  if ({values_f['name']}.size() != {indices_f['name']}.size()) {{"
            )
            lines.append(
                f'{ind}    throw std::invalid_argument("{setter_name}: values/indices size mismatch");'
            )
            lines.append(f"{ind}  }}")

        args = []
        for f in fields:
            if f is None:
                continue
            args.append(f"{f['name']}.data()")
            args.append(f"static_cast<i_t>({f['name']}.size())")
        lines.append(f"{ind}  cpu_problem.{setter_name}({', '.join(args)});")
        lines.append(f"{ind}}}")
        lines.append("")

    # Non-grouped arrays
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        name = f["name"]
        if name in grouped_fields or f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        afid = _field_array_id_name(f)
        setter = _default_problem_setter(f)

        enum_key = _is_repeated_enum(registry, ftype)
        if enum_key:
            edef = _lookup_enum(registry, enum_key)
            from_fn = _enum_from_proto_fn(enum_key, edef)
            cpp_type = _enum_cpp_type(enum_key, edef)
            lines.append(
                f"{ind}auto {name}_ints = get_ints(cuopt::remote::{afid});"
            )
            lines.append(f"{ind}if (!{name}_ints.empty()) {{")
            lines.append(f"{ind}  std::vector<{cpp_type}> {name};")
            lines.append(f"{ind}  {name}.reserve({name}_ints.size());")
            lines.append(f"{ind}  for (auto v : {name}_ints) {{")
            lines.append(
                f"{ind}    {name}.push_back({from_fn}(static_cast<cuopt::remote::{_enum_proto_type(enum_key, edef)}>(v)));"
            )
            lines.append(f"{ind}  }}")
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}.data(), static_cast<i_t>({name}.size()));"
            )
            lines.append(f"{ind}}}")
        elif ftype == "repeated string":
            lines.append(
                f"{ind}auto {name} = get_string_list(cuopt::remote::{afid});"
            )
            lines.append(
                f"{ind}if (!{name}.empty()) {{ cpu_problem.{setter}({name}); }}"
            )
        elif ftype == "bytes":
            lines.append(
                f"{ind}auto {name}_str = get_bytes(cuopt::remote::{afid});"
            )
            lines.append(f"{ind}if (!{name}_str.empty()) {{")
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}_str.data(), static_cast<i_t>({name}_str.size()));"
            )
            lines.append(f"{ind}}}")
        elif ftype.startswith("repeated"):
            extract_fn = "get_doubles" if "double" in ftype else "get_ints"
            lines.append(
                f"{ind}auto {name} = {extract_fn}(cuopt::remote::{afid});"
            )
            lines.append(f"{ind}if (!{name}.empty()) {{")
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}.data(), static_cast<i_t>({name}.size()));"
            )
            lines.append(f"{ind}}}")
        lines.append("")

    # repeated_messages — reconstruct each entry from scalars (carried in
    # `header`) + arrays (carried in `container_arrays`, keyed by
    # (container_field_num, container_index, container-relative field_id)).
    rm_entries = list(_iter_repeated_messages(obj))
    if rm_entries:
        lines.append("")
        lines.append(
            f"{ind}auto container_get_doubles = [&](int32_t _cfn, "
            f"int32_t _ci, int32_t _fid) -> std::vector<f_t> {{"
        )
        lines.append(
            f"{ind}  auto it = container_arrays.find(container_array_key_t{{_cfn, _ci, _fid}});"
        )
        lines.append(
            f"{ind}  if (it == container_arrays.end() || it->second.empty()) return {{}};"
        )
        lines.append(f"{ind}  if (it->second.size() % sizeof(double) != 0) {{")
        lines.append(
            f'{ind}    throw std::invalid_argument("container_get_doubles: payload size " '
            f'+ std::to_string(it->second.size()) + " is not a multiple of sizeof(double)");'
        )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}  size_t n = it->second.size() / sizeof(double);")
        lines.append(f"{ind}  if constexpr (std::is_same_v<f_t, double>) {{")
        lines.append(f"{ind}    std::vector<double> v(n);")
        lines.append(
            f"{ind}    std::memcpy(v.data(), it->second.data(), n * sizeof(double));"
        )
        lines.append(f"{ind}    return v;")
        lines.append(f"{ind}  }} else {{")
        lines.append(f"{ind}    std::vector<double> tmp(n);")
        lines.append(
            f"{ind}    std::memcpy(tmp.data(), it->second.data(), n * sizeof(double));"
        )
        lines.append(
            f"{ind}    return std::vector<f_t>(tmp.begin(), tmp.end());"
        )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}}};")
        lines.append("")
        lines.append(
            f"{ind}auto container_get_ints = [&](int32_t _cfn, "
            f"int32_t _ci, int32_t _fid) -> std::vector<i_t> {{"
        )
        lines.append(
            f"{ind}  auto it = container_arrays.find(container_array_key_t{{_cfn, _ci, _fid}});"
        )
        lines.append(
            f"{ind}  if (it == container_arrays.end() || it->second.empty()) return {{}};"
        )
        lines.append(
            f"{ind}  if (it->second.size() % sizeof(int32_t) != 0) {{"
        )
        lines.append(
            f'{ind}    throw std::invalid_argument("container_get_ints: payload size " '
            f'+ std::to_string(it->second.size()) + " is not a multiple of sizeof(int32_t)");'
        )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}  size_t n = it->second.size() / sizeof(int32_t);")
        lines.append(f"{ind}  if constexpr (std::is_same_v<i_t, int32_t>) {{")
        lines.append(f"{ind}    std::vector<int32_t> v(n);")
        lines.append(
            f"{ind}    std::memcpy(v.data(), it->second.data(), n * sizeof(int32_t));"
        )
        lines.append(f"{ind}    return v;")
        lines.append(f"{ind}  }} else {{")
        lines.append(f"{ind}    std::vector<int32_t> tmp(n);")
        lines.append(
            f"{ind}    std::memcpy(tmp.data(), it->second.data(), n * sizeof(int32_t));"
        )
        lines.append(
            f"{ind}    return std::vector<i_t>(tmp.begin(), tmp.end());"
        )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}}};")

        for name, body in rm_entries:
            setter = body.get("setter")
            cpp_inner = body.get("cpp_inner_type")
            cfn = body["field_num"]
            if not setter or not cpp_inner:
                raise ValueError(
                    f"repeated_messages.{name}: requires both 'setter' and "
                    f"'cpp_inner_type' for chunked decode"
                )
            lines.append("")
            lines.append(f"{ind}if (header.{name}_size() > 0) {{")
            lines.append(f"{ind}  std::vector<{cpp_inner}> _entries;")
            lines.append(f"{ind}  _entries.reserve(header.{name}_size());")
            lines.append(
                f"{ind}  for (int32_t _ci = 0; _ci < header.{name}_size(); ++_ci) {{"
            )
            lines.append(
                f"{ind}    const auto& pb_entry = header.{name}(_ci);"
            )
            lines.append(f"{ind}    {cpp_inner} _entry;")
            for f in _repeated_message_scalars(body):
                sp = _proto_cpp_name(f["name"])
                member = f.get("member", f["name"])
                from_cast = f.get("from_proto_cast")
                ftype = f.get("type", "double")
                if from_cast:
                    lines.append(
                        f"{ind}    _entry.{member} = static_cast<{from_cast}>(pb_entry.{sp}());"
                    )
                elif _from_proto_cast(ftype):
                    lines.append(
                        f"{ind}    _entry.{member} = static_cast<{_from_proto_cast(ftype)}>(pb_entry.{sp}());"
                    )
                else:
                    lines.append(
                        f"{ind}    _entry.{member} = pb_entry.{sp}();"
                    )
            for f in _repeated_message_arrays(body):
                member = f.get("member", f["name"])
                array_id = f.get("array_id")
                if array_id is None:
                    raise ValueError(
                        f"repeated_messages.{name}.arrays.{f['name']}: "
                        "missing required 'array_id' (container-relative routing id)"
                    )
                ftype = f.get("type", "repeated double")
                fn = (
                    "container_get_ints"
                    if "int32" in ftype
                    else "container_get_doubles"
                )
                lines.append(
                    f"{ind}    _entry.{member} = {fn}({cfn}, _ci, {array_id});"
                )
            for a, b in _repeated_message_companion_pairs(name, body):
                a_mem = a.get("member", a["name"])
                b_mem = b.get("member", b["name"])
                lines.append(
                    f"{ind}    if (_entry.{a_mem}.size() != _entry.{b_mem}.size()) {{"
                )
                lines.append(
                    f'{ind}      throw std::invalid_argument("{setter}: {a["name"]}/'
                    f'{b["name"]} size mismatch");'
                )
                lines.append(f"{ind}    }}")
            if name == "quadratic_constraints":
                lines.append(
                    f"{ind}    io::canonicalize_coo_matrix(_entry.rows, _entry.cols, _entry.vals);"
                )
            lines.append(f"{ind}    _entries.push_back(std::move(_entry));")
            lines.append(f"{ind}  }}")
            lines.append(f"{ind}  cpu_problem.{setter}(std::move(_entries));")
            lines.append(f"{ind}}}")

    return "\n".join(lines).rstrip()


def _gen_build_array_chunk_requests(registry, indent="  "):
    """Generate body of build_array_chunk_requests()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = [
        f"{ind}std::vector<cuopt::remote::SendArrayChunkRequest> requests;"
    ]
    lines.append("")
    setter_groups = obj.get("setter_groups", {})
    grouped_fields = set()
    for gdef in setter_groups.values():
        for fname in gdef.get("fields", []):
            grouped_fields.add(fname)

    def _emit_chunk_call(f, getter_expr, var_name, ii):
        ftype = f.get("type", "repeated double")
        afid = _field_array_id_name(f)

        if ftype == "repeated string":
            lines.append(f"{ii}{{")
            lines.append(f"{ii}  auto _blob = names_to_blob({getter_expr});")
            lines.append(
                f"{ii}  chunk_byte_blob(requests, cuopt::remote::{afid}, _blob, upload_id, chunk_size_bytes);"
            )
            lines.append(f"{ii}}}")
        elif ftype == "bytes":
            lines.append(f"{ii}if (!{var_name}.empty()) {{")
            lines.append(
                f"{ii}  std::vector<uint8_t> _bytes({var_name}.begin(), {var_name}.end());"
            )
            lines.append(
                f"{ii}  chunk_byte_blob(requests, cuopt::remote::{afid}, _bytes, upload_id, chunk_size_bytes);"
            )
            lines.append(f"{ii}}}")
        elif "int32" in ftype:
            lines.append(
                f"{ii}chunk_typed_array(requests, cuopt::remote::{afid}, {var_name}, upload_id, chunk_size_bytes);"
            )
        elif _is_repeated_enum(registry, ftype):
            lines.append(
                f"{ii}chunk_typed_array(requests, cuopt::remote::{afid}, {var_name}, upload_id, chunk_size_bytes);"
            )
        else:
            lines.append(
                f"{ii}chunk_typed_array(requests, cuopt::remote::{afid}, {var_name}, upload_id, chunk_size_bytes);"
            )

    # Non-grouped arrays
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        name = f["name"]
        if name in grouped_fields or f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        getter = _default_problem_getter(f, is_scalar=False)

        enum_key = _is_repeated_enum(registry, ftype)
        if enum_key:
            edef = _lookup_enum(registry, enum_key)
            to_fn = _enum_to_proto_fn(enum_key, edef)
            afid = _field_array_id_name(f)
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = problem.{getter};")
            lines.append(f"{ind}  if (!_{name}.empty()) {{")
            lines.append(f"{ind}    std::vector<int32_t> _{name}_ints;")
            lines.append(f"{ind}    _{name}_ints.reserve(_{name}.size());")
            lines.append(
                f"{ind}    for (const auto& v : _{name}) _{name}_ints.push_back(static_cast<int32_t>({to_fn}(v)));"
            )
            lines.append(
                f"{ind}    chunk_typed_array(requests, cuopt::remote::{afid}, _{name}_ints, upload_id, chunk_size_bytes);"
            )
            lines.append(f"{ind}  }}")
            lines.append(f"{ind}}}")
        elif ftype == "repeated string":
            _emit_chunk_call(f, f"problem.{getter}", None, ind)
        elif ftype == "bytes":
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = problem.{getter};")
            _emit_chunk_call(f, None, f"_{name}", ind + "  ")
            lines.append(f"{ind}}}")
        else:
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = problem.{getter};")
            _emit_chunk_call(f, None, f"_{name}", ind + "  ")
            lines.append(f"{ind}}}")

    # Setter groups
    for gname, gdef in setter_groups.items():
        fields = [
            _find_problem_field(obj, fn) for fn in gdef.get("fields", [])
        ]
        for f in fields:
            if f is None:
                continue
            getter = _default_problem_getter(f, is_scalar=False)
            afid = _field_array_id_name(f)
            lines.append(f"{ind}{{")
            lines.append(
                f"{ind}  const auto& _{f['name']} = problem.{getter};"
            )
            lines.append(
                f"{ind}  chunk_typed_array(requests, cuopt::remote::{afid}, _{f['name']}, upload_id, chunk_size_bytes);"
            )
            lines.append(f"{ind}}}")

    # repeated_messages — for each entry, emit one chunk-batch per array
    # field, tagged with the container's (field_num, index) so the worker
    # can route chunks back to the right entry.
    for name, body in _iter_repeated_messages(obj):
        presence = body.get("presence_check")
        getter = body.get("getter")
        if not presence or not getter:
            continue
        cfn = body["field_num"]
        lines.append("")
        lines.append(f"{ind}if (problem.{presence}) {{")
        lines.append(f"{ind}  const auto& _entries = problem.{getter};")
        lines.append(
            f"{ind}  for (size_t _idx = 0; _idx < _entries.size(); ++_idx) {{"
        )
        lines.append(f"{ind}    const auto& _entry = _entries[_idx];")
        lines.append(f"{ind}    int32_t _ci = static_cast<int32_t>(_idx);")
        for f in _repeated_message_arrays(body):
            member = f.get("member", f["name"])
            array_id = f.get("array_id")
            if array_id is None:
                raise ValueError(
                    f"repeated_messages.{name}.arrays.{f['name']}: "
                    "missing required 'array_id' (container-relative routing id)"
                )
            lines.append(
                f"{ind}    chunk_container_typed_array(requests, {cfn}, _ci, "
                f"{array_id}, _entry.{member}, upload_id, chunk_size_bytes);"
            )
        lines.append(f"{ind}  }}")
        lines.append(f"{ind}}}")

    lines.append("")
    lines.append(f"{ind}return requests;")
    return "\n".join(lines)


