def _problem_getter_root(f):
    """Get the getter root for a problem field (setter_getter_root or name)."""
    return f.get("setter_getter_root", f["name"])


def _default_problem_getter(f, is_scalar):
    """Default getter for an optimization_problem field."""
    if "getter" in f:
        return f["getter"]
    root = _problem_getter_root(f)
    if is_scalar:
        return f"get_{root}()"
    ftype = f.get("type", "repeated double")
    if ftype == "repeated string":
        return f"get_{root}()"
    return f"get_{root}_host()"


def _default_problem_setter(f):
    """Default setter name for an optimization_problem field."""
    if "setter" in f:
        return f["setter"]
    root = _problem_getter_root(f)
    return f"set_{root}"


def _find_problem_field(obj, name):
    """Find a field by name in the optimization_problem arrays list."""
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        if f["name"] == name:
            return f
    return None


def _group_presence_field(fields):
    """Pick the structural sentinel for a setter group. For CSR-style groups
    (values/indices/offsets), offsets is always non-empty even when nnz=0, so
    it's the correct presence sentinel. Falls back to the first field."""
    for f in fields:
        if f is not None and f["name"].endswith("_offsets"):
            return f
    return next((f for f in fields if f is not None), None)


def _group_companion_pair(fields):
    """Identify the (values, indices) pair within a CSR-style setter group,
    used to validate that companion arrays have matching lengths. Returns
    (values_field, indices_field) or (None, None) if not applicable."""
    values = next(
        (f for f in fields if f is not None and f["name"].endswith("_values")),
        None,
    )
    indices = next(
        (
            f
            for f in fields
            if f is not None and f["name"].endswith("_indices")
        ),
        None,
    )
    return values, indices


def _field_array_id_name(f):
    """Derive ArrayFieldId enum name from field name: FIELD_{UPPER_SNAKE(name)}."""
    return f"FIELD_{f['name'].upper()}"


def _field_result_id_name(f, prefix=""):
    """Derive ResultFieldId enum name from field name: RESULT_{PREFIX_}{UPPER_SNAKE(name)}."""
    if prefix:
        return f"RESULT_{prefix}_{f['name'].upper()}"
    return f"RESULT_{f['name'].upper()}"


def _problem_field_proto_type(registry, ftype):
    """Map optimization_problem field type to proto type."""
    if not ftype:
        return "double"
    enum_key = _is_repeated_enum(registry, ftype)
    if enum_key:
        edef = _lookup_enum(registry, enum_key)
        return f"repeated {_enum_proto_type(enum_key, edef)}"
    if ftype in (
        "repeated double",
        "repeated int32",
        "repeated string",
        "bytes",
    ):
        return ftype
    edef = _lookup_enum(registry, ftype)
    if edef:
        return _enum_proto_type(ftype, edef)
    return ftype


def _settings_field_proto_type(registry, f):
    """Map settings field type to proto type."""
    ftype = f.get("type", "double")
    edef = _lookup_enum(registry, ftype)
    if edef:
        return _enum_proto_type(ftype, edef)
    return ftype


def _solution_scalar_proto_type(registry, f):
    """Map solution scalar type to proto type."""
    ftype = f.get("type", "double")
    edef = _lookup_enum(registry, ftype)
    if edef:
        return _enum_proto_type(ftype, edef)
    return ftype


