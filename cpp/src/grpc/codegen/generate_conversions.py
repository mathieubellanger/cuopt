def generate_enum_proto_from_registry(registry):
    """Generate all enum definitions for the proto file.

    Skips enum entries without 'values' (e.g. result_field_id which is
    auto-derived and only carries aliases).
    """
    enums = registry.get("enums", {})
    blocks = []
    for key, edef in enums.items():
        if "values" not in edef:
            continue
        proto_type_name = _enum_proto_type(key, edef)
        prefix = edef.get("proto_prefix", "")
        lines = [f"enum {proto_type_name} {{"]
        for cpp_name, num in parse_enum_values(edef["values"]):
            pname = _proto_enum_value_name(cpp_name, prefix)
            lines.append(f"  {pname} = {num};")
        lines.append("}")
        blocks.append("\n".join(lines))
    return "\n\n".join(blocks)


def generate_enum_converters_inc(registry, domain=None):
    """Generate C++ to_proto/from_proto switch functions for enums.

    If domain is given, only emit converters for enums with that domain tag.
    If domain is None, emit all converters (backward compat).
    Skips enum entries without 'values' (auto-derived enums like result_field_id).
    """
    enums = registry.get("enums", {})
    funcs = []
    for key, edef in enums.items():
        if "values" not in edef:
            continue
        if domain is not None and edef.get("domain") != domain:
            continue
        cpp_type = _enum_cpp_type(key, edef)
        proto_type_name = _enum_proto_type(key, edef)
        to_fn = _enum_to_proto_fn(key, edef)
        from_fn = _enum_from_proto_fn(key, edef)
        prefix = edef.get("proto_prefix", "")

        # Omit `default:` so -Wswitch-enum flags any newly-added enumerator that
        # was not handled. Throw after the switch to reject unknown wire/int
        # values at runtime.
        lines = [
            f"cuopt::remote::{proto_type_name} {to_fn}({cpp_type} v)",
            "{",
            "  switch (v) {",
        ]
        for cpp_name, _ in parse_enum_values(edef["values"]):
            pname = _proto_enum_value_name(cpp_name, prefix)
            lines.append(
                f"    case {cpp_type}::{cpp_name}: return cuopt::remote::{pname};"
            )
        lines.extend(
            [
                "  }",
                f'  throw std::invalid_argument("Unknown {cpp_type}: " + std::to_string(static_cast<int>(v)));',
                "}",
            ]
        )
        funcs.append("\n".join(lines))

        lines = [
            f"{cpp_type} {from_fn}(cuopt::remote::{proto_type_name} v)",
            "{",
            "  switch (v) {",
        ]
        for cpp_name, _ in parse_enum_values(edef["values"]):
            pname = _proto_enum_value_name(cpp_name, prefix)
            lines.append(
                f"    case cuopt::remote::{pname}: return {cpp_type}::{cpp_name};"
            )
        lines.extend(
            [
                "  }",
                f'  throw std::invalid_argument("Unknown cuopt::remote::{proto_type_name}: " + std::to_string(static_cast<int>(v)));',
                "}",
            ]
        )
        funcs.append("\n".join(lines))

    return "\n\n".join(funcs)


