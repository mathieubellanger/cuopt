# Ranges for auto-numbering.  hi=None means no upper bound (own message).
# Only solution scalars need hard caps because they share ChunkedResultHeader.
# Solution array_ids share a global ResultFieldId pool (handled separately).
FIELD_NUM_RANGES = {
    "optimization_problem.field_num": (1, None),
    "optimization_problem.array_id": (0, None),
    "lp_solution.scalars": (1000, 1999),
    "lp_solution.arrays.field_num": (1, None),
    "mip_solution.scalars": (2000, 2999),
    "mip_solution.arrays.field_num": (1, None),
    "lp_solution.warm_start.scalars": (3000, 3999),
    "lp_solution.warm_start.arrays.field_num": (1, None),
    "pdlp_settings": (1, None),
    "mip_settings": (1, None),
}


def _collect_field_nums(entries, key_name="field_num"):
    """Collect all existing field_num or array_id values from a list of field entries."""
    nums = set()
    for entry in entries:
        if isinstance(entry, dict) and len(entry) == 1:
            name = next(iter(entry))
            val = entry[name]
            if isinstance(val, dict) and key_name in val:
                nums.add(val[key_name])
    return nums


def _collect_settings_field_nums(entries):
    """Collect field_num values from settings fields (handles nesting)."""
    nums = set()
    for entry in entries:
        if isinstance(entry, dict) and len(entry) == 1:
            name = next(iter(entry))
            val = entry[name]
            if isinstance(val, list):
                nums |= _collect_settings_field_nums(val)
            elif isinstance(val, dict) and "field_num" in val:
                nums.add(val["field_num"])
    return nums


def _embed_field_nums(section):
    """Return the set of tags pinned by submessage embeds on a section."""
    nums = set()
    for embed in section.get("embeds") or []:
        if isinstance(embed, dict) and "field_num" in embed:
            nums.add(embed["field_num"])
    return nums


def _iter_named_field_nums(entries, key_name="field_num"):
    """Yield (num, source_label) for field-list entries carrying key_name."""
    for entry in entries or []:
        if isinstance(entry, dict) and len(entry) == 1:
            name = next(iter(entry))
            val = entry[name]
            if isinstance(val, dict) and key_name in val:
                yield val[key_name], name


def _iter_named_settings_field_nums(entries, parent=""):
    """Yield (num, source_label) for nested settings entries."""
    for entry in entries or []:
        if isinstance(entry, dict) and len(entry) == 1:
            name = next(iter(entry))
            val = entry[name]
            label = f"{parent}.{name}" if parent else name
            if isinstance(val, list):
                yield from _iter_named_settings_field_nums(val, label)
            elif isinstance(val, dict) and "field_num" in val:
                yield val["field_num"], label


def _iter_named_embed_field_nums(section):
    for embed in section.get("embeds") or []:
        if (
            isinstance(embed, dict)
            and "field_num" in embed
            and "name" in embed
        ):
            yield embed["field_num"], f"embed:{embed['name']}"


def _check_unique(label, pairs, errors):
    """Record duplicate values found in (num, source) pairs under `label`."""
    seen = {}
    for num, src in pairs:
        if num in seen:
            errors.append(
                f"{label}: duplicate tag {num} "
                f"(conflict: {seen[num]!r} vs {src!r})"
            )
        else:
            seen[num] = src


def _validate_registry_uniqueness(registry):
    """Raise ValueError if the registry assigns the same tag twice within any
    proto message's field namespace, or the same array_id twice in the shared
    ResultFieldId pool. Aliases declared under `enums.result_field_id.aliases`
    are excluded (they are intentional allow_alias entries)."""
    errors = []

    # Per-message field_num pools. Each tuple: (proto message label, section).
    per_message = [
        ("OptimizationProblem", "optimization_problem", ["scalars", "arrays"]),
        ("LPSolution", "lp_solution", ["scalars", "arrays"]),
        ("MIPSolution", "mip_solution", ["scalars", "arrays"]),
    ]
    for msg, key, field_keys in per_message:
        section = registry.get(key) or {}
        pairs = []
        for fk in field_keys:
            pairs.extend(_iter_named_field_nums(section.get(fk, [])))
        pairs.extend(_iter_named_embed_field_nums(section))
        _check_unique(msg, pairs, errors)

    # Settings messages use the nested `fields:` layout.
    for msg, key in [
        ("PDLPSolverSettings", "pdlp_settings"),
        ("MIPSolverSettings", "mip_settings"),
    ]:
        section = registry.get(key) or {}
        pairs = list(
            _iter_named_settings_field_nums(section.get("fields", []))
        )
        pairs.extend(_iter_named_embed_field_nums(section))
        _check_unique(msg, pairs, errors)

    # PDLPWarmStartData is currently sourced from lp_solution.warm_start; if
    # mip_solution ever grows a warm_start block it will be the same message
    # and tags must not collide with the LP side.
    ws_pairs = []
    for key in ["lp_solution", "mip_solution"]:
        ws = (registry.get(key) or {}).get("warm_start") or {}
        for fk in ["scalars", "arrays"]:
            for num, name in _iter_named_field_nums(ws.get(fk, [])):
                ws_pairs.append((num, f"{key}.warm_start.{fk}.{name}"))
    _check_unique("PDLPWarmStartData", ws_pairs, errors)

    # Global ResultFieldId pool — shared across all solution/warm_start arrays.
    # Aliases live under enums.result_field_id.aliases and piggyback on these
    # primary numbers via proto's `allow_alias`; they are not part of this pool.
    rid_pairs = []
    for key in ["lp_solution", "mip_solution"]:
        section = registry.get(key) or {}
        for num, name in _iter_named_field_nums(
            section.get("arrays", []), "array_id"
        ):
            rid_pairs.append((num, f"{key}.arrays.{name}"))
        ws = section.get("warm_start") or {}
        for num, name in _iter_named_field_nums(
            ws.get("arrays", []), "array_id"
        ):
            rid_pairs.append((num, f"{key}.warm_start.arrays.{name}"))
    _check_unique("ResultFieldId", rid_pairs, errors)

    # ArrayFieldId pool — keys problem-side chunked array payloads. Duplicates
    # would make chunked uploads ambiguous (two fields share a payload slot).
    problem = registry.get("optimization_problem") or {}
    afid_pairs = [
        (num, f"optimization_problem.arrays.{name}")
        for num, name in _iter_named_field_nums(
            problem.get("arrays", []), "array_id"
        )
    ]
    _check_unique("ArrayFieldId", afid_pairs, errors)

    # `optional: true` is meaningful for any proto3 settings field whose C++
    # default differs from the proto3 zero value. The codegen emits
    # `optional <type>` + a `has_<X>()` guard for the scalar settings path.
    # It composes with `sentinel`: the optional guard handles "omit ⇒ keep
    # default" while the sentinel guard handles "reserved value ⇒ keep
    # default" (e.g. -1 for iteration_limit / node_limit). It also composes
    # with enum-typed fields: the `has_<X>()` guard wraps the enum
    # from_proto conversion so an omitted field preserves the C++ default
    # for enums whose proto3 zero (the first value listed in the registry)
    # does not coincide with the documented C++ default — e.g.
    # pdlp_solver_mode (proto-zero=Stable1, C++ default=Stable3).

    if errors:
        raise ValueError(
            "field_registry.yaml validation failed:\n  - "
            + "\n  - ".join(errors)
        )


def _ruamel_insert(mapping, key, value):
    """Add a key to a CommentedMap at its conventional position.

    field_num goes first (position 0), array_id goes right after field_num,
    everything else appends."""
    if not hasattr(mapping, "insert"):
        mapping[key] = value
        return
    if key == "field_num":
        mapping.insert(0, key, value)
    elif key == "array_id":
        pos = 1 if "field_num" in mapping else 0
        mapping.insert(pos, key, value)
    else:
        mapping[key] = value


def _assign_to_field_list(entries, key_name, lo, hi, existing, label):
    """Assign missing key_name values to fields in a list. Mutates entries in-place.
    hi=None means no upper bound."""
    next_num = max(existing) + 1 if existing else lo
    assigned = 0
    for idx, entry in enumerate(entries):
        if isinstance(entry, str):
            continue
        if not isinstance(entry, dict) or len(entry) != 1:
            continue
        name = next(iter(entry))
        val = entry[name]
        if val is None:
            try:
                from ruamel.yaml.comments import CommentedMap

                entry[name] = CommentedMap([(key_name, next_num)])
            except ImportError:
                entry[name] = {key_name: next_num}
            existing.add(next_num)
            next_num += 1
            assigned += 1
        elif isinstance(val, dict) and key_name not in val:
            _ruamel_insert(val, key_name, next_num)
            existing.add(next_num)
            next_num += 1
            assigned += 1
    if hi is not None and next_num - 1 > hi and assigned > 0:
        raise ValueError(
            f"Range overflow in {label}: assigned {next_num - 1} exceeds max {hi}"
        )
    return assigned


def _assign_to_settings_fields(entries, lo, hi, existing, label):
    """Assign missing field_num to settings fields (handles nesting). Mutates in-place.
    hi=None means no upper bound."""
    try:
        from ruamel.yaml.comments import CommentedMap as _CMap
    except ImportError:
        _CMap = None
    next_num = max(existing) + 1 if existing else lo
    assigned = 0
    for entry in entries:
        if not isinstance(entry, dict) or len(entry) != 1:
            continue
        name = next(iter(entry))
        val = entry[name]
        if isinstance(val, list):
            sub_assigned = _assign_to_settings_fields(
                val, lo, hi, existing, f"{label}.{name}"
            )
            assigned += sub_assigned
            next_num = max(existing) + 1 if existing else next_num
        elif val is None:
            if _CMap is not None:
                entry[name] = _CMap([("field_num", next_num)])
            else:
                entry[name] = {"field_num": next_num}
            existing.add(next_num)
            next_num += 1
            assigned += 1
        elif isinstance(val, dict) and "field_num" not in val:
            _ruamel_insert(val, "field_num", next_num)
            existing.add(next_num)
            next_num += 1
            assigned += 1
    if hi is not None and next_num - 1 > hi and assigned > 0:
        raise ValueError(
            f"Range overflow in {label}: assigned {next_num - 1} exceeds max {hi}"
        )
    return assigned


def _normalize_bare_strings(entries):
    """Convert bare string entries in a field list to single-key dicts.
    This lets the auto-numbering logic treat them uniformly.

    Avoids modifying CommentedSeq comment attributes (ca.items) to prevent
    corrupting ruamel.yaml's internal comment tracking on dump."""
    try:
        from ruamel.yaml.comments import CommentedMap

        mk_inner = CommentedMap

        def mk_outer(name, inner):
            return CommentedMap([(name, inner)])

    except ImportError:
        mk_inner = dict

        def mk_outer(name, inner):
            return {name: inner}

    for idx, entry in enumerate(entries):
        if isinstance(entry, str):
            entries[idx] = mk_outer(entry, mk_inner())


_STRIP_RE = re.compile(r"^ +(?:field_num|array_id): +\d+ *\n", re.MULTILINE)


def strip_field_numbers_text(path):
    """Remove field_num/array_id lines from the registry via text substitution.

    Pure text operation — guaranteed to preserve all comments and formatting.
    Returns the number of lines removed."""
    with open(path) as f:
        original = f.read()
    stripped, count = _STRIP_RE.subn("", original)
    if count > 0:
        with open(path, "w") as f:
            f.write(stripped)
    return count


def _dump_and_verify(ryaml, data, path):
    """Dump YAML data to a string buffer, verify comments survived, then write.

    Never truncates the original file until the output is validated."""
    from io import StringIO

    with open(path) as f:
        original = f.read()
    original_comments = [
        line for line in original.splitlines() if line.lstrip().startswith("#")
    ]

    buf = StringIO()
    ryaml.dump(data, buf)
    written = buf.getvalue()
    written_comments = [
        line for line in written.splitlines() if line.lstrip().startswith("#")
    ]

    if len(written_comments) < len(original_comments):
        print(
            f"ERROR: comment loss detected in {path} "
            f"({len(original_comments)} → {len(written_comments)} comment lines). "
            f"File not modified.",
            file=sys.stderr,
        )
        sys.exit(1)

    with open(path, "w") as f:
        f.write(written)


def _registry_has_field_numbers(registry):
    """Check if the registry has at least one field_num or array_id assigned."""
    for section_key in list(registry.keys()):
        section = registry.get(section_key)
        if not isinstance(section, dict):
            continue
        for list_key in ["scalars", "arrays"]:
            for nums in [
                _collect_field_nums(section.get(list_key, []), "field_num"),
                _collect_field_nums(section.get(list_key, []), "array_id"),
            ]:
                if nums:
                    return True
        if "fields" in section:
            if _collect_settings_field_nums(section["fields"]):
                return True
        sub = section.get("warm_start")
        if isinstance(sub, dict):
            for list_key in ["scalars", "arrays"]:
                for key in ["field_num", "array_id"]:
                    if _collect_field_nums(sub.get(list_key, []), key):
                        return True
    return False


def auto_assign_field_numbers(data):
    """Walk the registry and assign missing field_num / array_id values.

    Operates on ruamel.yaml CommentedMap data in-place.
    Returns the total number of assignments made.
    """
    # Normalize bare string entries to dicts before processing
    for section_key in list(data.keys()):
        section = data.get(section_key)
        if not isinstance(section, dict):
            continue
        for list_key in ["scalars", "arrays", "fields"]:
            entries = section.get(list_key)
            if entries:
                _normalize_bare_strings(entries)
        for sub_key in ["warm_start"]:
            sub = section.get(sub_key)
            if isinstance(sub, dict):
                for list_key in ["scalars", "arrays"]:
                    entries = sub.get(list_key)
                    if entries:
                        _normalize_bare_strings(entries)

    total = 0

    for section_key in ["optimization_problem"]:
        section = data.get(section_key)
        if not section:
            continue

        # field_num — single contiguous pool across scalars and arrays
        scalars = section.get("scalars", [])
        arrays = section.get("arrays", [])
        lo, hi = FIELD_NUM_RANGES[f"{section_key}.field_num"]
        existing_fn = _collect_field_nums(
            scalars, "field_num"
        ) | _collect_field_nums(arrays, "field_num")
        total += _assign_to_field_list(
            scalars,
            "field_num",
            lo,
            hi,
            existing_fn,
            f"{section_key}.scalars.field_num",
        )
        total += _assign_to_field_list(
            arrays,
            "field_num",
            lo,
            hi,
            existing_fn,
            f"{section_key}.arrays.field_num",
        )

        # array_id — separate namespace
        lo, hi = FIELD_NUM_RANGES[f"{section_key}.array_id"]
        existing_aid = _collect_field_nums(arrays, "array_id")
        total += _assign_to_field_list(
            arrays,
            "array_id",
            lo,
            hi,
            existing_aid,
            f"{section_key}.arrays.array_id",
        )

    # Collect all solution array_ids into a shared pool (ResultFieldId is global).
    shared_result_ids = set()
    all_solution_array_lists = []
    for section_key in ["lp_solution", "mip_solution"]:
        section = data.get(section_key)
        if not section:
            continue
        arr = section.get("arrays", [])
        shared_result_ids |= _collect_field_nums(arr, "array_id")
        all_solution_array_lists.append((section_key, arr))
        ws = section.get("warm_start")
        if ws:
            ws_arr = ws.get("arrays", [])
            shared_result_ids |= _collect_field_nums(ws_arr, "array_id")
            all_solution_array_lists.append(
                (f"{section_key}.warm_start", ws_arr)
            )

    for section_key in ["lp_solution", "mip_solution"]:
        section = data.get(section_key)
        if not section:
            continue

        # Scalars — field_num (range-bound for ChunkedResultHeader)
        scalars = section.get("scalars", [])
        lo, hi = FIELD_NUM_RANGES[f"{section_key}.scalars"]
        existing = _collect_field_nums(scalars, "field_num")
        total += _assign_to_field_list(
            scalars, "field_num", lo, hi, existing, f"{section_key}.scalars"
        )

        # Arrays — field_num (per-message, no cap). Reserve tags used by
        # embedded submessages so the auto-numberer can't hand them out to a
        # new array.
        arrays = section.get("arrays", [])
        arr_fn_range = FIELD_NUM_RANGES.get(
            f"{section_key}.arrays.field_num", (1, None)
        )
        existing_fn = _collect_field_nums(arrays, "field_num")
        existing_fn |= _embed_field_nums(section)
        total += _assign_to_field_list(
            arrays,
            "field_num",
            arr_fn_range[0],
            arr_fn_range[1],
            existing_fn,
            f"{section_key}.arrays.field_num",
        )

        # Arrays — array_id (shared ResultFieldId pool, no cap)
        total += _assign_to_field_list(
            arrays,
            "array_id",
            0,
            None,
            shared_result_ids,
            f"{section_key}.arrays.array_id",
        )

        # Warm start
        ws = section.get("warm_start")
        if ws:
            ws_scalars = ws.get("scalars", [])
            lo, hi = FIELD_NUM_RANGES.get(
                f"{section_key}.warm_start.scalars", (3000, 3999)
            )
            existing = _collect_field_nums(ws_scalars, "field_num")
            total += _assign_to_field_list(
                ws_scalars,
                "field_num",
                lo,
                hi,
                existing,
                f"{section_key}.warm_start.scalars",
            )

            ws_arrays = ws.get("arrays", [])
            ws_fn_range = FIELD_NUM_RANGES.get(
                f"{section_key}.warm_start.arrays.field_num", (1, None)
            )
            existing_fn = _collect_field_nums(ws_arrays, "field_num")
            total += _assign_to_field_list(
                ws_arrays,
                "field_num",
                ws_fn_range[0],
                ws_fn_range[1],
                existing_fn,
                f"{section_key}.warm_start.arrays.field_num",
            )

            # WS arrays — array_id (shared ResultFieldId pool)
            total += _assign_to_field_list(
                ws_arrays,
                "array_id",
                0,
                None,
                shared_result_ids,
                f"{section_key}.warm_start.arrays.array_id",
            )

    for section_key in ["pdlp_settings", "mip_settings"]:
        section = data.get(section_key)
        if not section:
            continue
        fields = section.get("fields", [])
        lo, hi = FIELD_NUM_RANGES[section_key]
        existing = _collect_settings_field_nums(fields)
        # Reserve tags used by embedded submessages so auto-numbering won't
        # collide with them.
        existing |= _embed_field_nums(section)
        total += _assign_to_settings_fields(
            fields, lo, hi, existing, section_key
        )

    return total


