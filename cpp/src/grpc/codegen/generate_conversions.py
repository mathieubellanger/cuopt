def _gen_problem_to_proto(registry, indent="  "):
    """Generate body of map_problem_to_proto()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []
    setter_groups = obj.get("setter_groups", {})
    grouped_fields = set()
    for gdef in setter_groups.values():
        for fname in gdef.get("fields", []):
            grouped_fields.add(fname)

    # Scalars
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        pname = _proto_cpp_name(f["name"])
        getter = _default_problem_getter(f, is_scalar=True)
        ftype = f.get("type", "double")
        edef = _lookup_enum(registry, ftype)
        if edef:
            to_fn = _enum_to_proto_fn(ftype, edef)
            lines.append(
                f"{ind}pb_problem->set_{pname}({to_fn}(cpu_problem.{getter}));"
            )
        else:
            lines.append(
                f"{ind}pb_problem->set_{pname}(cpu_problem.{getter});"
            )

    lines.append("")

    # Non-grouped arrays
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        if name in grouped_fields or f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        getter = _default_problem_getter(f, is_scalar=False)

        enum_key = _is_repeated_enum(registry, ftype)
        if enum_key:
            edef = _lookup_enum(registry, enum_key)
            to_fn = _enum_to_proto_fn(enum_key, edef)
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = cpu_problem.{getter};")
            lines.append(
                f"{ind}  for (const auto& v : _{name}) pb_problem->add_{pname}({to_fn}(v));"
            )
            lines.append(f"{ind}}}")
        elif ftype == "repeated string":
            lines.append(
                f"{ind}for (const auto& s : cpu_problem.{getter}) pb_problem->add_{pname}(s);"
            )
        elif ftype == "bytes":
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{name} = cpu_problem.{getter};")
            lines.append(f"{ind}  if (!_{name}.empty()) {{")
            lines.append(
                f"{ind}    pb_problem->set_{pname}(std::string(_{name}.begin(), _{name}.end()));"
            )
            lines.append(f"{ind}  }}")
            lines.append(f"{ind}}}")
        elif ftype.startswith("repeated"):
            cast = _array_element_cast(ftype)
            var = f"_{name}"
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto {var} = cpu_problem.{getter};")
            lines.append(
                f"{ind}  for (const auto& v : {var}) pb_problem->add_{pname}(static_cast<{cast}>(v));"
            )
            lines.append(f"{ind}}}")

    # Setter groups
    for gname, gdef in setter_groups.items():
        fields = [
            _find_problem_field(obj, fn) for fn in gdef.get("fields", [])
        ]
        for f in fields:
            if f is None:
                continue
            pname = _proto_cpp_name(f["name"])
            getter = _default_problem_getter(f, is_scalar=False)
            ftype = f.get("type", "repeated double")
            cast = _array_element_cast(ftype)
            lines.append(f"{ind}{{")
            lines.append(f"{ind}  auto _{f['name']} = cpu_problem.{getter};")
            lines.append(
                f"{ind}  for (const auto& v : _{f['name']}) pb_problem->add_{pname}(static_cast<{cast}>(v));"
            )
            lines.append(f"{ind}}}")

    return "\n".join(lines)


def _gen_proto_to_problem(registry, indent="  "):
    """Generate body of map_proto_to_problem()."""
    obj = registry.get("optimization_problem", {})
    ind = indent
    lines = []
    setter_groups = obj.get("setter_groups", {})
    grouped_fields = set()
    for gdef in setter_groups.values():
        for fname in gdef.get("fields", []):
            grouped_fields.add(fname)
    # Scalars
    for entry in obj.get("scalars", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        setter = _default_problem_setter(f)
        ftype = f.get("type", "double")
        edef = _lookup_enum(registry, ftype)
        if edef:
            from_fn = _enum_from_proto_fn(ftype, edef)
            lines.append(
                f"{ind}cpu_problem.{setter}({from_fn}(pb_problem.{pname}()));"
            )
        else:
            lines.append(f"{ind}cpu_problem.{setter}(pb_problem.{pname}());")

    lines.append("")

    # Setter groups — guard on the structural sentinel (offsets). For CSR-style
    # groups, offsets is non-empty even when nnz=0; using values/indices as the
    # sentinel would silently drop zero-nnz matrices.
    for _gname, gdef in setter_groups.items():
        setter_name = gdef["setter"]
        fields = [
            _find_problem_field(obj, fn) for fn in gdef.get("fields", [])
        ]
        sentinel = _group_presence_field(fields)
        if sentinel is None:
            continue
        sentinel_pname = _proto_cpp_name(sentinel["name"])
        lines.append(f"{ind}if (pb_problem.{sentinel_pname}_size() > 0) {{")
        ii = ind + "  "

        for f in fields:
            if f is None:
                continue
            pname = _proto_cpp_name(f["name"])
            ftype = f.get("type", "repeated double")
            cpp_t = _array_cpp_type(ftype)
            lines.append(
                f"{ii}std::vector<{cpp_t}> {f['name']}(pb_problem.{pname}().begin(), pb_problem.{pname}().end());"
            )

        values_f, indices_f = _group_companion_pair(fields)
        if values_f is not None and indices_f is not None:
            lines.append(
                f"{ii}if ({values_f['name']}.size() != {indices_f['name']}.size()) {{"
            )
            lines.append(
                f'{ii}  throw std::invalid_argument("{setter_name}: values/indices size mismatch");'
            )
            lines.append(f"{ii}}}")

        args = []
        for f in fields:
            if f is None:
                continue
            args.append(f"{f['name']}.data()")
            args.append(f"static_cast<i_t>({f['name']}.size())")
        lines.append(f"{ii}cpu_problem.{setter_name}({', '.join(args)});")
        lines.append(f"{ind}}}")
        lines.append("")

    # Non-grouped arrays
    for entry in obj.get("arrays", []):
        f = parse_field(entry)
        name = f["name"]
        pname = _proto_cpp_name(name)
        if name in grouped_fields or f.get("skip_conversion"):
            continue
        ftype = f.get("type", "repeated double")
        setter = _default_problem_setter(f)

        enum_key = _is_repeated_enum(registry, ftype)
        if enum_key:
            edef = _lookup_enum(registry, enum_key)
            from_fn = _enum_from_proto_fn(enum_key, edef)
            cpp_type = _enum_cpp_type(enum_key, edef)
            lines.append(f"{ind}if (pb_problem.{pname}_size() > 0) {{")
            lines.append(f"{ind}  std::vector<{cpp_type}> {name};")
            lines.append(f"{ind}  {name}.reserve(pb_problem.{pname}_size());")
            proto_type = _enum_proto_type(enum_key, edef)
            lines.append(
                f"{ind}  for (const auto& v : pb_problem.{pname}()) {{"
            )
            lines.append(
                f"{ind}    {name}.push_back({from_fn}(static_cast<cuopt::remote::{proto_type}>(v)));"
            )
            lines.append(f"{ind}  }}")
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}.data(), static_cast<i_t>({name}.size()));"
            )
            lines.append(f"{ind}}}")
        elif ftype == "repeated string":
            lines.append(f"{ind}if (pb_problem.{pname}_size() > 0) {{")
            lines.append(
                f"{ind}  std::vector<std::string> {name}(pb_problem.{pname}().begin(), pb_problem.{pname}().end());"
            )
            lines.append(f"{ind}  cpu_problem.{setter}({name});")
            lines.append(f"{ind}}}")
        elif ftype == "bytes":
            lines.append(f"{ind}if (!pb_problem.{pname}().empty()) {{")
            lines.append(
                f"{ind}  const std::string& {name}_str = pb_problem.{pname}();"
            )
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}_str.data(), static_cast<i_t>({name}_str.size()));"
            )
            lines.append(f"{ind}}}")
        elif ftype.startswith("repeated"):
            cpp_t = _array_cpp_type(ftype)
            guard = f.get("conditional")
            open_brace = (
                f"if (pb_problem.{pname}_size() > 0) {{" if guard else "{"
            )
            lines.append(f"{ind}{open_brace}")
            lines.append(
                f"{ind}  std::vector<{cpp_t}> {name}(pb_problem.{pname}().begin(), pb_problem.{pname}().end());"
            )
            lines.append(
                f"{ind}  cpu_problem.{setter}({name}.data(), static_cast<i_t>({name}.size()));"
            )
            lines.append(f"{ind}}}")

    return "\n".join(lines)


